package com.experian.bepartner.payload;


public class Product {
	private String VName;
	private String VIdProductType;
	private String VProductcode;
	private String VDescription;

	public String getVName() {
		return VName;
	}

	public void setVName(String vName) {
		VName = vName;
	}

	public String getVIdProductType() {
		return VIdProductType;
	}

	public void setVIdProductType(String vIdProductType) {
		VIdProductType = vIdProductType;
	}

	public String getVProductcode() {
		return VProductcode;
	}

	public void setVProductcode(String vProductcode) {
		VProductcode = vProductcode;
	}

	public String getVDescription() {
		return VDescription;
	}

	public void setVDescription(String vDescription) {
		VDescription = vDescription;
	}

}
